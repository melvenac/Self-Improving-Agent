#!/usr/bin/env node
// Scan vault experiences for skill candidates (3+ similar experiences).
// Groups by tags and uses Smart Connections for semantic clustering.
// Usage: node vault-skill-scan.mjs

import { existsSync, readdirSync, readFileSync, writeFileSync } from 'fs';
import { join } from 'path';
import { homedir } from 'os';
import { execSync } from 'child_process';
import { EXPERIENCES_DIR, VAULT_PATH, log } from './vault-utils.mjs';

const SKILL_THRESHOLD = 3;
const CANDIDATES_FILE = join(VAULT_PATH, 'Guidelines', 'SKILL-CANDIDATES.md');

function main() {
  console.log('=== Scanning for skill candidates ===\n');

  if (!existsSync(EXPERIENCES_DIR)) {
    console.log('No Experiences directory found');
    return;
  }

  const files = readdirSync(EXPERIENCES_DIR).filter(f => f.endsWith('.md'));
  console.log(`Found ${files.length} experience files\n`);

  // Parse all experiences
  const experiences = [];
  for (const file of files) {
    const content = readFileSync(join(EXPERIENCES_DIR, file), 'utf-8');
    const fm = parseFrontmatter(content);
    experiences.push({ file, ...fm, content });
  }

  // --- Method 1: Tag clustering ---
  const tagGroups = {};
  for (const exp of experiences) {
    for (const tag of exp.tags) {
      if (!tagGroups[tag]) tagGroups[tag] = [];
      tagGroups[tag].push(exp);
    }
  }

  // Filter to groups meeting threshold, exclude noise tags
  const noiseTags = new Set(['test', 'marker', 'green-flamingo', 'purple-octopus', 'blue-whale', 'red-parrot', 'global', 'project-scoped', 'session-summary']);
  const tagCandidates = Object.entries(tagGroups)
    .filter(([tag, exps]) => exps.length >= SKILL_THRESHOLD && !noiseTags.has(tag))
    .sort((a, b) => b[1].length - a[1].length);

  // --- Method 2: Project clustering ---
  const projectGroups = {};
  for (const exp of experiences) {
    if (!exp.project || exp.project === 'unknown') continue;
    if (!projectGroups[exp.project]) projectGroups[exp.project] = [];
    projectGroups[exp.project].push(exp);
  }
  const projectCandidates = Object.entries(projectGroups)
    .filter(([, exps]) => exps.length >= SKILL_THRESHOLD)
    .sort((a, b) => b[1].length - a[1].length);

  // --- Method 3: Type clustering ---
  const typeGroups = {};
  for (const exp of experiences) {
    if (!exp.type) continue;
    if (!typeGroups[exp.type]) typeGroups[exp.type] = [];
    typeGroups[exp.type].push(exp);
  }
  const typeCandidates = Object.entries(typeGroups)
    .filter(([, exps]) => exps.length >= SKILL_THRESHOLD)
    .sort((a, b) => b[1].length - a[1].length);

  // --- Build report ---
  const now = new Date().toISOString().slice(0, 10);
  let report = `---\ndate: ${now}\ntype: skill-scan\n---\n\n# Skill Candidates\n\n> Auto-generated by \`vault-skill-scan.mjs\` on ${now}.  \n> Clusters of ${SKILL_THRESHOLD}+ experiences suggest a reusable skill could be distilled.\n\n`;

  if (tagCandidates.length === 0 && projectCandidates.length === 0) {
    report += '_No clusters meeting the threshold were found._\n';
    console.log('No skill candidates found.');
  } else {
    // Tag clusters
    if (tagCandidates.length > 0) {
      report += '## By Tag\n\n';
      for (const [tag, exps] of tagCandidates) {
        report += `### ${tag} (${exps.length} experiences)\n\n`;
        report += `**Potential skill:** "${tag}" patterns and gotchas\n\n`;
        for (const exp of exps) {
          const trigger = extractTrigger(exp.content);
          report += `- [[${exp.file.replace('.md', '')}]] — ${trigger}\n`;
        }
        report += '\n';
      }
    }

    // Project clusters
    if (projectCandidates.length > 0) {
      report += '## By Project\n\n';
      for (const [project, exps] of projectCandidates) {
        report += `### ${project} (${exps.length} experiences)\n\n`;
        for (const exp of exps) {
          const trigger = extractTrigger(exp.content);
          report += `- [[${exp.file.replace('.md', '')}]] — ${trigger}\n`;
        }
        report += '\n';
      }
    }

    // Type clusters
    if (typeCandidates.length > 0) {
      report += '## By Type\n\n';
      for (const [type, exps] of typeCandidates) {
        report += `### ${type} (${exps.length} experiences)\n\n`;
        for (const exp of exps) {
          const trigger = extractTrigger(exp.content);
          report += `- [[${exp.file.replace('.md', '')}]] — ${trigger}\n`;
        }
        report += '\n';
      }
    }
  }

  report += `---\n\n*Next scan: run \`node vault-skill-scan.mjs\` or wait for periodic check.*\n`;

  // Write report
  writeFileSync(CANDIDATES_FILE, report);
  console.log(`\nReport written to: ${CANDIDATES_FILE}`);

  // Print summary
  console.log('\n=== Summary ===');
  for (const [tag, exps] of tagCandidates) {
    console.log(`  Tag "${tag}": ${exps.length} experiences → SKILL CANDIDATE`);
  }
  for (const [project, exps] of projectCandidates) {
    console.log(`  Project "${project}": ${exps.length} experiences → SKILL CANDIDATE`);
  }
}

function parseFrontmatter(content) {
  const result = { tags: [], project: '', type: '' };
  const fmMatch = content.match(/^---\n([\s\S]*?)\n---/);
  if (!fmMatch) return result;

  const fm = fmMatch[1];

  const tagMatch = fm.match(/tags:\s*\[(.*?)\]/);
  if (tagMatch) {
    result.tags = tagMatch[1].split(',').map(t => t.trim()).filter(Boolean);
  }

  const projMatch = fm.match(/project:\s*(.+)/);
  if (projMatch) result.project = projMatch[1].trim();

  const typeMatch = fm.match(/type:\s*(.+)/);
  if (typeMatch) result.type = typeMatch[1].trim();

  return result;
}

function extractTrigger(content) {
  const match = content.match(/## Trigger\n+(.+)/);
  if (match) return match[1].trim().slice(0, 100);
  return '(no trigger)';
}

main();
